       DRMAJSE=ABS(DRMAJSA-DRMAJSO)
       DRMAJFE=ABS(DRMAJFA-DRMAJFO)
      IF(DRMAJSO.GT.0.) DRMAJSE=DRMAJSE/DRMAJSO
      IF(DRMAJFO.GT.0.) DRMAJFE=DRMAJFE/DRMAJFO
