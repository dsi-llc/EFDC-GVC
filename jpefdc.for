       IF(ISTRAN(6).EQ.0.AND.ISTRAN(7).EQ.1)THEN
CJHFIX-REMOVE         WRITE(3,101)NJP,NJE,TIME,SALJNE,TEMJNE,(SNDJNE(NX),NX=1,NSND)
CJHFIX-ADD THE FOLLOWING 2 LINE(S)
         WRITE(3,101)NJP,NJE,TIME,SALJNE,TEMJNE,DYEJNE,
