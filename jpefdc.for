      IF(NPRTE.EQ.1)THEN
      IF(IOUTJP(NJP).GE.2)THEN
        NJPS=NJPS+1
        TIMJP(NJPS)=TIME
        XJGNS(NJPS)=XJGNE
        YJGNS(NJPS)=YJGNE
        ZJGNS(NJPS)=ZJGNE
        SIGNS(NJPS)=SIGNE
        RADJNS(NJPS)=RADJNE
        RLEJNS(NJPS)=RLEJNE
        QJNS(NJPS)=QJ(NE)
        UJGNS(NJPS)=UJG(NE)
        VJGNS(NJPS)=VJG(NE)
        WJGNS(NJPS)=WJG(NE)
        RDQANS(NJPS)=RDQA(NE)
        RMAJPNS(NJPS)=RMAJP(NE)
        DRMAJNS(NJPS)=DRMAJ
        DRHONS(NJPS)=DRHO
        SALJNS(NJPS)=SALJNE
        TEMJNS(NJPS)=TEMJNE
